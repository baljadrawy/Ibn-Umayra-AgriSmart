# مؤشر تقييم الملاءمة حسب النجم والمنطقة
ZONE_SCORE_MODIFIER = {
    'highlands': 0,   # المرجع الأصلي
    'central':   -5,  # أكثر جفافاً وحرارة
    'east':       5,  # رطوبة تساعد الربيع والخريف
    'west':       8,  # ساحلي أكثر ملاءمة لفترة أطول
    'north':    -10,  # أبرد — الصقيع يمتد
}

def get_farming_score(nawaa_name: str, zone_id: str = 'highlands') -> int:
    """يحسب مؤشر ملاءمة الزراعة من 0-100 حسب النجم والمنطقة"""
    base = NAWAA_RECOMMENDATIONS.get(nawaa_name, {}).get('farming_score', 50)
    modifier = ZONE_SCORE_MODIFIER.get(zone_id, 0)
    return max(0, min(100, base + modifier))

def score_label(score: int) -> str:
    if score >= 80: return "🟢 ممتاز — وقت مثالي للزراعة"
    if score >= 60: return "🟡 جيد — زراعة مناسبة مع الاحتياط"
    if score >= 35: return "🟠 متوسط — للمحاصيل المتحملة فقط"
    return "🔴 ضعيف — تجنب الزراعة الجديدة"


def get_zone_for_city(city: str) -> dict | None:
    for zid, zone in CLIMATE_ZONES.items():
        if city in zone['cities']:
            return {'id': zid, **zone}
    return None


def get_nearest_city(lat: float, lon: float) -> str:
    min_dist = float('inf')
    nearest = 'الطائف'
    for city, (clat, clon) in CITIES_COORDINATES.items():
        d = math.sqrt((lat - clat) ** 2 + (lon - clon) ** 2)
        if d < min_dist:
            min_dist = d
            nearest = city
    return nearest


def get_adjusted_nawaa(offset_days: int = 0) -> dict | None:
    today = date.today()
    effective = today - timedelta(days=offset_days)
    match_str = effective.strftime('%m-%d')

    current = None
    for n in CALENDAR_2026:
        if n['start'] <= match_str <= n['end']:
            current = n
            break

    if not current:
        return None

    year = today.year
    sm, sd = map(int, current['start'].split('-'))
    em, ed = map(int, current['end'].split('-'))
    base_start = date(year, sm, sd)
    base_end = date(year, em, ed)
    adj_start = base_start + timedelta(days=offset_days)
    adj_end = base_end + timedelta(days=offset_days)

    duration = (base_end - base_start).days + 1
    elapsed = (today - adj_start).days + 1
    progress = min(100, max(0, round((elapsed / duration) * 100)))
    remaining = max(0, duration - elapsed)

    temp = NAWAA_TEMPERATURE.get(current['id'], {'avg': 21, 'label': '21°م معتدل', 'wind': 'متنوعة', 'rain': '10%'})
    recs = NAWAA_RECOMMENDATIONS.get(current['name'], {
        'planting': [], 'activities': [], 'warnings': [],
        'best_days': '—', 'farming_score': 50,
        'best_crops_season': '—', 'soil_advice': '—'
    })

    def fmt_date(d):
        ar_months = {
            'January':'يناير','February':'فبراير','March':'مارس','April':'أبريل',
            'May':'مايو','June':'يونيو','July':'يوليو','August':'أغسطس',
            'September':'سبتمبر','October':'أكتوبر','November':'نوفمبر','December':'ديسمبر'
        }
        s = d.strftime('%-d %B')
        for en, ar in ar_months.items():
            s = s.replace(en, ar)
        return s

    return {
        'id': current['id'],
        'name': current['name'],
        'cycle': current['cycle'],
        'start_date': fmt_date(adj_start),
        'end_date': fmt_date(adj_end),
        'day_in_nawaa': max(1, elapsed),
        'days_remaining': remaining,
        'progress': progress,
        'duration': duration,
        'temp_avg': temp['avg'],
        'temp_label': temp['label'],
        'wind': temp['wind'],
        'rain': temp['rain'],
        'note': current['note'],
        'planting': recs['planting'],
        'activities': recs['activities'],
        'warnings': recs['warnings'],
        'best_days': recs.get('best_days', '—'),
        'farming_score': recs.get('farming_score', 50),
        'best_crops_season': recs.get('best_crops_season', '—'),
        'soil_advice': recs.get('soil_advice', '—'),
    }
