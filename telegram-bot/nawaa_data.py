def get_zone_for_city(city: str) -> dict | None:
    for zid, zone in CLIMATE_ZONES.items():
        if city in zone['cities']:
            return {'id': zid, **zone}
    return None


def get_nearest_city(lat: float, lon: float) -> str:
    min_dist = float('inf')
    nearest = 'الطائف'
    for city, (clat, clon) in CITIES_COORDINATES.items():
        d = math.sqrt((lat - clat) ** 2 + (lon - clon) ** 2)
        if d < min_dist:
            min_dist = d
            nearest = city
    return nearest


def get_adjusted_nawaa(offset_days: int = 0) -> dict | None:
    today = date.today()
    effective = today - timedelta(days=offset_days)
    match_str = effective.strftime('%m-%d')

    current = None
    for n in CALENDAR_2026:
        if n['start'] <= match_str <= n['end']:
            current = n
            break

    if not current:
        return None

    year = today.year
    sm, sd = map(int, current['start'].split('-'))
    em, ed = map(int, current['end'].split('-'))
    base_start = date(year, sm, sd)
    base_end = date(year, em, ed)
    adj_start = base_start + timedelta(days=offset_days)
    adj_end = base_end + timedelta(days=offset_days)

    duration = (base_end - base_start).days + 1
    elapsed = (today - adj_start).days + 1
    progress = min(100, max(0, round((elapsed / duration) * 100)))
    remaining = max(0, duration - elapsed)

    temp = NAWAA_TEMPERATURE.get(current['id'], {'avg': 21, 'label': '21°م معتدل', 'wind': 'متنوعة', 'rain': '10%'})
    recs = NAWAA_RECOMMENDATIONS.get(current['name'], {'planting': [], 'activities': [], 'warnings': []})

    return {
        'id': current['id'],
        'name': current['name'],
        'cycle': current['cycle'],
        'start_date': adj_start.strftime('%-d %B').replace('January','يناير').replace('February','فبراير')
                                                    .replace('March','مارس').replace('April','أبريل')
                                                    .replace('May','مايو').replace('June','يونيو')
                                                    .replace('July','يوليو').replace('August','أغسطس')
                                                    .replace('September','سبتمبر').replace('October','أكتوبر')
                                                    .replace('November','نوفمبر').replace('December','ديسمبر'),
        'end_date': adj_end.strftime('%-d %B').replace('January','يناير').replace('February','فبراير')
                                               .replace('March','مارس').replace('April','أبريل')
                                               .replace('May','مايو').replace('June','يونيو')
                                               .replace('July','يوليو').replace('August','أغسطس')
                                               .replace('September','سبتمبر').replace('October','أكتوبر')
                                               .replace('November','نوفمبر').replace('December','ديسمبر'),
        'day_in_nawaa': max(1, elapsed),
        'days_remaining': remaining,
        'progress': progress,
        'duration': duration,
        'temp_avg': temp['avg'],
        'temp_label': temp['label'],
        'wind': temp['wind'],
        'rain': temp['rain'],
        'note': current['note'],
        'planting': recs['planting'],
        'activities': recs['activities'],
        'warnings': recs['warnings'],
    }
